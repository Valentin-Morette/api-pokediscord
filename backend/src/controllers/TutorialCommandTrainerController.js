const models = require("../models");

class TutorialCommandTrainerController {
  static browse = (req, res) => {
    models.tutorial_command_trainer
      .findAll()
      .then(([rows]) => {
        res.send(rows);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static read = (req, res) => {
    models.tutorial_command_trainer
      .find(req.params.id)
      .then(([rows]) => {
        if (rows[0] == null) {
          res.sendStatus(404);
        } else {
          res.send(rows[0]);
        }
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static readByTrainer = (req, res) => {
    models.tutorial_command_trainer
      .findByTrainer(req.params.id)
      .then(([rows]) => {
        res.send(rows);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static edit = (req, res) => {
    const tutorialCommandTrainer = req.body;

    tutorialCommandTrainer.id = parseInt(req.params.id, 10);

    models.tutorial_command_trainer
      .update(tutorialCommandTrainer)
      .then(([result]) => {
        if (result.affectedRows === 0) {
          res.sendStatus(404);
        } else {
          res.sendStatus(204);
        }
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static add = (req, res) => {
    const tutorialCommandTrainer = req.body;

    models.tutorial_command_trainer
      .insert(tutorialCommandTrainer)
      .then(([result]) => {
        res
          .status(201)
          .send({ ...tutorialCommandTrainer, id: result.insertId });
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };

  static delete = (req, res) => {
    models.tutorial_command_trainer
      .delete(req.params.id)
      .then(() => {
        res.sendStatus(204);
      })
      .catch((err) => {
        console.error(err);
        res.sendStatus(500);
      });
  };
}

module.exports = TutorialCommandTrainerController;
